    display: flex;
    justify-content: space-between;
    align-items: center;
    padding: 4px var(--spacing-xs);
    border-bottom: 1px solid var(--color-border);
    .right {
      display: flex;
      align-items: center;
      gap: var(--spacing-xs);
      .button {
        cursor: default;
      }
    }
  }
  iframe {
    background-color: #fff;
  }
}
</style>
